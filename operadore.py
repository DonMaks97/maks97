#operadores numericos
print("ingresar 2 numeros enteros \n")
A = int(input("introduce un numero"))
B = int(input("introduce otro numero"))

#suma
print("la suma de", A, "y", B, "es igual a:")
print(A+B)

#resta
print("la resta de", A, "y ", B, "es igual a:")
print(A-B)

#multiplicacion
print("la multiplicacion de", A, "y ", B, "es igual a:")
print(A*B)

#division
print("la division de", A, "y ", B, "es igual a:")
print(A/B)

#division solo entero
print("la division con entero de", A, "y ", B, "es igual a:")
print(A//B)

#division, resto
print("la resto de la division de", A, "y ", B, "es igual a:")
print(A%B)

#elevacion
print("la elevacion de", A, "y ", B, "es igual a:")
print(A**B)

